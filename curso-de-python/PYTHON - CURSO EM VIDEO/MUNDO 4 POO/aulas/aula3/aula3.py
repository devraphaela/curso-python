# AULA 03 - Guia Definitivo - Classes, Objetos, Atributos, etc


# CLASSE = uma "forma", um formato a ser seguido sempre que eu precisar fazer objetos do mesmo tipo ou que tenham as mesmas características. 
    # forminha de biscoito = classe
    # biscoito = objeto

    # para criar uma classe, preciso de 3 coisas (diagrama de classes):
        # nomeClasse
        # características que tenho (atributos)
        # coisas que posso fazer (métodos)

    # EXEMPLO:
        # biscoitoCoracao
        # atributos
            # + tamanho = 8.2cm
            # + massa = baunilha
            # + peso = 54.3g
            # + cobertura = pistache
            # + cozido = True
            # + temperatura = 55 graus
        # métodos
            # + cozinhar()
            # + congelar()
            # + cobrir()
            # + confeitar()
            # + podeComer()
            # + comer()


# INSTÂNCIA = instanciar é seguir o padrão que foi definido na classe pra poder criar um objeto (construir).
    # "O objeto é a instância de uma classe"


# OBJETO = é uma coisa material ou abstrata que é feita a partir de um modelo(classe) e pode ser descrita por meio de suas características(atributos), comportamentos(métodos) e estado atual.


# ESTADO = um conjunto de valores desses atributos ao passar do tempo.


# Exemplos de objetos abstratos: 
    # consultaMedico
    # atributos
        # endereço
        # horário
        # nomeMedico
        # horarioComeca
        # horarioTermina
        # atendimentoAconteceu
        # receitas
    # métodos
        # inciarConsulta()
        # terminarConsulta()
        # adiarConsulta()
        # cancelarConsulta()

    # erroSistema
    # atributos
        # quandoAconteceu = False(estado)
        # tipoErro = tal
        # codErro = blabla
        # causaErro = etc
        # conseqErro = etc
    #métodos
        # dispararErro()
        # corrigirErro()
